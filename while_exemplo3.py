#criação de um loop

i = 0

#Lembrando que o 0 conta como um valor na memoria, então para aparecer até o numero 10, teremos que adicionar 11.
#Consede o valor de I para a repetição até 11.

while (i < 11):

    print("Mais uma mensagem com 'I', valendo: {}".format(i))

#Vai somar o valor até o numero solicitado dentro da condição da repetição (while).

    i = i + 1